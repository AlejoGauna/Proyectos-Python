print("Ingresar tres numeros enteros, calcular su promedio y mostrarlo")

#ingresar valores
primero = int(input("Ingresar el primer numero entero :"))
segundo = int(input("Ingresar el segundo numero entero :"))
tercero = int(input("Ingresar el tercero numero entero :"))

suma = primero + segundo + tercero

#imprimir por pantalla el promedio
print("El promedio calculado es : ", suma / 3)
